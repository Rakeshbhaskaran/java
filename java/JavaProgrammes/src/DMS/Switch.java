package DMS;

public class Switch {
	public static void main(String[] args) {
		String a ="10.56";
		switch(a){
		case "10.56" : System.out.println("Equal");
		break;
		case "23": System.out.println("Unequal");
		break;
		case "56": System.out.println("Unequal");
		break;
		default: System.out.println("Unequal");
		break;
	}

}
}
